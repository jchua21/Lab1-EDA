public class JavaOperators {
    public static void main(String[] args) {
        System.out.println("========  OPERADORES ========");
        int a = 20;
        int b = 5;
        System.out.println("Suma(+) de " + a + " y " + b + " es " + (a+b));
        System.out.println("Resta(-) de " + a + " y " + b + " es " + (a-b));
        System.out.println("Multiplicacion(*) de " + a + " y " + b + " es " + (a*b));
        System.out.println("Division(/) de " + a + " y " + b + " es " + (a/b));
        System.out.println("Modulo(%) de " + a + " y " + b + " es " + (a%b));

        System.out.println("Puedes incrementar en 1 el valor de una variable usando x++");
        System.out.println("Puedes decrementar en 1 el valor de una variable usando x--");

        System.out.println("x += 5 es lo mismo que x = x + 5");
        System.out.println("x -= 5 es lo mismo que x = x - 5");
        System.out.println("x *= 5 es lo mismo que x = x * 5");
        System.out.println("x /= 5 es lo mismo que x = x / 5");
        System.out.println("x %= 5 es lo mismo que x = x % 5");
    }
}
