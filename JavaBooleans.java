public class JavaBooleans {
    public static void main(String[] args){
        //Los valores booleanos son un tipo de datos que solo pueden ser verdadero o falso
        
        boolean javaEsDivertido = true;

        //Las expresiones booleanas son expresiones las cuales regresan verdadero o falso
        //Se pueden usar los signos <, > para crear estas expresiones

        System.out.println(9<10);//Output: true

        //Tambien se puede usar ==

        int num = 9;
        System.out.println(9==num);//Output: true
    }
}
